"""Generate the FLASH-seq UMI analysis pipeline (protocol section 12) as a bash stub.

The commands are transcribed from the protocol. Parameters that the protocol
exposes (UMI length, CTAAC spacer, STAR sjdbOverhang rule, featureCounts flags,
samtools -F 260) are pulled from protocol_params.yaml so this stays a single source
of truth. External tools (bcl2fastq, umi_tools, STAR, samtools, featureCounts,
bbmap) are NOT bundled -- the script preflights for them and marks each as external.
"""

from __future__ import annotations

from pathlib import Path

from ..params import Params


def generate_pipeline(p: Params) -> str:
    a = p.protocol["analysis"]
    fc = a["featurecounts"]
    umi_len = a["umi_length_bp"]
    spacer = a["spacer"]
    flag = a["samtools_filter_flag"]

    return f"""#!/usr/bin/env bash
# =============================================================================
# flashseq_pipeline.sh -- FLASH-seq UMI analysis (protocol section 12)
# Method: FLASH-seq UMI v3, DOI 10.17504/protocols.io.bp2l619rdvqe/v3 (CC-BY).
# Generated by flashseq-skill (MIT). RESEARCH USE ONLY -- not clinically validated.
#
# This is a runnable STUB. Adapt paths, threads, and RAM to your machine. The
# pipeline will "evolve with the question at hand" (protocol note).
# EXTERNAL DEPENDENCIES (install separately): bcl2fastq, umi_tools, STAR,
# samtools, featureCounts (subread), bbmap (optional trimming).
# =============================================================================
set -euo pipefail

# ---- 0. Variables (EDIT THESE) ---------------------------------------------
BASECALL_DIR="${{BASECALL_DIR:-/path/to/flowcell/Data/Intensities/BaseCalls/}}"
OUTPUT_DIR="${{OUTPUT_DIR:-./out}}"
SAMPLESHEET="${{SAMPLESHEET:-/path/to/Demultiplexing_SampleSheet.csv}}"
GENOME_DIR="${{GENOME_DIR:-/path/to/STAR_indexed_genome/}}"
FASTA="${{FASTA:-GRCh38.primary_assembly.genome.fa}}"          # src: 12.2 (Gencode/ENSEMBL)
GTF="${{GTF:-gencode.v34.primary_assembly.annotation.gtf}}"
READ_LENGTH="${{READ_LENGTH:-100}}"                              # src: 12.2 sjdbOverhang = read_len-1
SJDB_OVERHANG=$(( READ_LENGTH - 1 ))                            # rule: {a['star_sjdb_overhang_rule']}
SAMPLE_ID="${{SAMPLE_ID:-sample_id}}"
THREADS="${{THREADS:-10}}"
BBMAP_filter="${{BBMAP_filter:-filterbyname.sh}}"               # bbmap (optional)

# ---- Preflight: external tools ---------------------------------------------
for tool in bcl2fastq umi_tools STAR samtools featureCounts; do
  if ! command -v "$tool" >/dev/null 2>&1; then
    echo "[flashseq] MISSING external tool: $tool (install it; see protocol 12 prerequisites)" >&2
  fi
done

# ---- 1. bcl2fastq (12.1) ----------------------------------------------------
ulimit -n 10000 || true
bcl2fastq --input-dir "$BASECALL_DIR" --output-dir "$OUTPUT_DIR" \\
  --sample-sheet "$SAMPLESHEET" --create-fastq-for-index-reads --no-lane-splitting

# ---- 2. Genome index (12.2) -- run ONCE per genome/read length --------------
# STAR --runThreadN 15 --runMode genomeGenerate --genomeDir "$GENOME_DIR" \\
#   --genomeFastaFiles "$FASTA" --sjdbGTFfile "$GTF" --sjdbOverhang "$SJDB_OVERHANG"

# ---- 3. UMI extraction (12.3) -- {umi_len} bp UMI, spacer "{spacer}", regex ------
# UMI is in R1 (and a small fraction in R2 from upstream tagmentation events).
umi_tools extract --extract-method=regex \\
  --bc-pattern="^(?P<discard_1>AAGCAGTGGTATCAACGCAGAGT|AGCAGTGGTATCAACGCAGAGT|GCAGTGGTATCAACGCAGAGT|CAGTGGTATCAACGCAGAGT|AGTGGTATCAACGCAGAGT|GTGGTATCAACGCAGAGT|TGGTATCAACGCAGAGT|GGTATCAACGCAGAGT|GTATCAACGCAGAGT|ATCAACGCAGAGT|CAACGCAGAGT|AACGCAGAGT|ACGCAGAGT|GCAGAGT|CAGAGT|AGAGT|GAGT|AGT|GT)(?P<umi_1>.{{{umi_len}}})(?P<discard_2>{spacer}GG)(?P<discard_3>G{{0,4}})" \\
  --stdin="$SAMPLE_ID".R1.fastq.gz --stdout=umi.UMIinR1.R1.fq \\
  --read2-in="$SAMPLE_ID".R2.fastq.gz --read2-out=umi.UMIinR1.R2.fq

umi_tools extract --extract-method=regex \\
  --bc-pattern="^(?P<discard_1>GAGT|AGT|GT)(?P<umi_1>.{{{umi_len}}})(?P<discard_2>{spacer}GG)(?P<discard_3>G{{0,4}})" \\
  --stdin="$SAMPLE_ID".R2.fastq.gz --stdout=umi.UMIinR2.R2.fq \\
  --read2-in="$SAMPLE_ID".R1.fastq.gz --read2-out=umi.UMIinR2.R1.fq

# (12.3 steps 3-4: filter reads that carry a UMI in BOTH R1 and R2 -- see protocol.)
# (12.4: derive internal reads by excluding UMI read IDs via $BBMAP_filter.)
# (12.5: reconcile orientation -> umi.R1.fq / umi.R2.fq. 12.6: optional bbduk trim.)

# ---- 4. Map UMI reads (12.7) ------------------------------------------------
mkdir -p STAR
STAR --runThreadN "$THREADS" --limitBAMsortRAM 20000000000 --genomeLoad LoadAndKeep \\
  --genomeDir "$GENOME_DIR" --readFilesIn umi.R1.fq umi.R2.fq --readFilesCommand cat \\
  --limitSjdbInsertNsj 2000000 --seedSearchStartLmax 30 \\
  --outFilterIntronMotifs RemoveNoncanonicalUnannotated \\
  --outSAMtype BAM SortedByCoordinate --outFileNamePrefix STAR/"$SAMPLE_ID"_UMI_

# -F {flag} filters unmapped + secondary alignments (12.7)
samtools view -@ 5 -Sb -F {flag} STAR/"$SAMPLE_ID"_UMI_Aligned.sortedByCoord.out.bam \\
  > "$SAMPLE_ID"_UMI.filtered.bam
samtools index "$SAMPLE_ID"_UMI.filtered.bam

# ---- 5. Map internal reads (12.8) -- same, without UMI handling --------------
# STAR ... --outFileNamePrefix STAR/"$SAMPLE_ID"_INTERNAL_   (see protocol 12.8)

# ---- 6. Count matrix -- UMI (12.10) -----------------------------------------
mkdir -p FEATURECOUNTS
featureCounts -T 1 -p -t {fc['feature_type']} -g {fc['attribute']} -s {fc['strand_umi']} \\
  --fracOverlap {fc['frac_overlap']} -a "$GTF" -R BAM \\
  -o FEATURECOUNTS/"$SAMPLE_ID"_Aligned.txt "$SAMPLE_ID"_UMI.filtered.bam
samtools sort -@ "$THREADS" \\
  FEATURECOUNTS/"$SAMPLE_ID"_UMI.filtered.bam.featureCounts.bam \\
  -o FEATURECOUNTS/"$SAMPLE_ID"_UMI.fc.sorted.bam
samtools index FEATURECOUNTS/"$SAMPLE_ID"_UMI.fc.sorted.bam
umi_tools count --per-gene --paired --gene-tag=XT --chimeric-pairs=discard \\
  --unpaired-reads=discard --assigned-status-tag=XS \\
  -I FEATURECOUNTS/"$SAMPLE_ID"_UMI.fc.sorted.bam \\
  -S FEATURECOUNTS/"$SAMPLE_ID".umi.counts.tsv.gz

# ---- 7. Count matrix -- internal (12.11), no -s/UMI dedup --------------------
# featureCounts -T 1 -p -t {fc['feature_type']} -g {fc['attribute']} --fracOverlap {fc['frac_overlap']} \\
#   -a "$GTF" -o FEATURECOUNTS/"$SAMPLE_ID"_INTERNAL_featureCounts.txt \\
#   STAR/"$SAMPLE_ID"_INTERNAL_Aligned.sortedByCoord.filtered.bam

# ---- 8. Downstream analysis (12.12) -> scanpy -------------------------------
# flashseq_analysis.py assembles FEATURECOUNTS/*.umi.counts.tsv.gz into an AnnData and
# runs QC -> normalize -> HVG -> PCA -> Leiden -> UMAP -> marker genes (external: scanpy).
if command -v python3 >/dev/null 2>&1 && python3 -c "import scanpy" 2>/dev/null; then
  python3 flashseq_analysis.py
else
  echo "[flashseq] Count matrices in FEATURECOUNTS/. Run: python3 flashseq_analysis.py (needs scanpy)."
fi
"""


def write_pipeline(p: Params, out_dir: Path | str) -> dict:
    from .analysis import write_analysis

    out = Path(out_dir)
    out.mkdir(parents=True, exist_ok=True)
    script = out / "flashseq_pipeline.sh"
    script.write_text(generate_pipeline(p), encoding="utf-8")
    script.chmod(0o755)
    result = {"pipeline": str(script)}
    result.update(write_analysis(p, out))   # also emit flashseq_analysis.py
    return result
